// ── CSV Session Log ───────────────────────────────────────────────────────
export function exportSessionCSV(sessionLog, subjects) {
  const escape = (v) => `"${String(v).replace(/"/g, '""')}"`;
  const header = ["Index","Date","Time","Subject","Duration(min)","Session Type","Notes"]
    .map(escape).join(",");
  const rows = sessionLog.map((e, i) => {
    const subj = subjects.find((s) => s.id === e.subjId);
    const d    = new Date(e.ts);
    return [
      escape(i + 1),
      escape(e.date),
      escape(d.toLocaleTimeString([], { hour:"2-digit", minute:"2-digit" })),
      escape(subj ? subj.name : "Untagged"),
      escape(Math.round(e.secs / 60)),
      escape("Pomodoro Focus"),
      escape(e.notes || ""),
    ].join(",");
  });
  const csv = [header, ...rows].join("\n");
  downloadBlob(new Blob([csv], { type:"text/csv" }), "arkmaester-session-log.csv");
}

// ── Session TXT Report ────────────────────────────────────────────────────
export function exportSessionTxt(entry, subjects) {
  const subj  = subjects.find((s) => s.id === entry.subjId);
  const mins  = Math.round(entry.secs / 60);
  const d     = new Date(entry.ts);
  const lines = [
    "═══════════════════════════════════",
    "       Arkmaester Session Report",
    "═══════════════════════════════════",
    `Subject         : ${subj ? subj.name : "Untagged"}`,
    `Duration        : ${mins} minutes`,
    `Date            : ${d.toLocaleDateString([], { weekday:"long", year:"numeric", month:"long", day:"numeric" })}`,
    `Time            : ${d.toLocaleTimeString([], { hour:"2-digit", minute:"2-digit" })}`,
    `Session type    : Pomodoro focus`,
    `Pomodoros today : ${entry.sessionIndex ?? 1}`,
    entry.notes ? `\nNotes:\n${entry.notes}` : "",
    "═══════════════════════════════════",
    "Generated by Arkmaester",
  ].join("\n");
  downloadBlob(new Blob([lines], { type:"text/plain" }), `arkmaester-session-${entry.id}.txt`);
}

