import { getSubjectName, formatTime } from "./helpers";

export function downloadBlob(content: string, filename: string, contentType: string) {
  const blob = new Blob([content], { type: contentType });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

export function exportSessionCSV(sessionLog: any[], subjects: any[]) {
  let csv = "ID,Date,Subject,Duration (secs),Time Format,Notes\n";
  sessionLog.forEach((entry) => {
    const subjName = getSubjectName(entry.subjId, subjects);
    const date = entry.date || "";
    const secs = entry.secs || 0;
    const formatted = formatTime(secs);
    const notesStr = entry.notes ? `"${entry.notes.replace(/"/g, '""')}"` : '""';
    csv += `${entry.id},${date},"${subjName}",${secs},${formatted},${notesStr}\n`;
  });
  downloadBlob(csv, "arkmaester-session-log.csv", "text/csv;charset=utf-8;");
}

